package demo_Package;

public class ArithmaticOperations 
{
	public static void main(String[] args) 
	{
		//addtion(+)
		byte a=10;
		byte b=20;
		
		int c=a+b;
		System.out.println(c);
		//subtraction(-)
		int n1=100;
		int n2=50;
		
		int n3=n1-n2;
		System.out.println(n3);
		
		
		float f1= 21.40f;
		float f2= 30.40f;
		
		float f3=f1+f2;
		System.out.println(f3);
		
		
		c=b-a;
		System.out.println(c);
		
		float f4=f2-f1;
		System.out.println(f4);
		
		//multiplication(*)
		
		int i1=5;
		int i2=10;
		int i3=i1*i2;
		System.out.println(i3);
		
		double d1=5;
		double d2=10;
		double d3=d1*d2;
		System.out.println(d3);
		
		//division(/)
		
		int i4=i2/i1;
		System.out.println(i4);
		
		//modulus(%)
		
		int p1=10;
		int p2=3;
		int p3=p1%p2;
		System.out.println(p3);
		
		//increment operator(++) //increment by 1
		
		int t=10;
		t++;  //ans 11
		
		//decrement operator(--) //decrement by 1
		int t1=10;
		t1--;  //ans 9
		
		//Assignment operator
		
		//[Variable Name] = [Value]
		
		
	}

}
