package demo_Package;

public class RelationalOperators 
{

	public static void main(String[] args) 
	
	{
		//Comparision Operator
	 // Greater than (>)
		
		int a=10;
		int b=20;
		
		boolean result =b>a;
		System.out.println(result);
		
	//Less than (<)
		
		result= a<b;
		result=10<20;
		System.out.println(result);
		
	//equal equal (==)
		
		result= (a==b);
		System.out.println(result);
		
	//Greater than or Equal to (>=)
		
		result = (a>=b);
		System.out.println(result);
		
	//Less than or Equal to (<=)
		
		result = (a<=b);
		System.out.println(result);
		
	//Not equal to (!=)
		
		result = (a!=b);
		System.out.println(result);
		
	
	}
}
